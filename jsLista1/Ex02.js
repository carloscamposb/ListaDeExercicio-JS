// 2) Faça um Programa que peça dois números e imprima a soma.

// declara as variáveis (num1 e num2) e transforma o numéro em tipo inteiro por meio do parseInt 
const num1= parseInt( prompt ("Informe o primeiro número: ")); 

const num2= parseInt(prompt ("Informe o segundo número: "));

// Cria uma variável chamada soma para armazenar o resultado da soma do (num1) e o (num2)
const somaNumeros= num1+ num2; 

// Mostra mensagem ao usuário com o resultado da soma dos números escritos.
alert ("O resultado da soma dos dois números foi: " + somaNumeros) 
